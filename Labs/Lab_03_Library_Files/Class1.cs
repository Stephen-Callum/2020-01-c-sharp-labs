using System;

namespace Lab_03_Library_Files
{
    // class is a container
    public class MyClass
    {
        public double GravitationalConstant = 9.81;
        
        // Method/Function
        public int DoubleUp(int num)
        {
            return 2 * num;
        }

        // Static equivalent
        public static int TripleUp(int num)
        {
            return 3 * num;
        }

        //field
        public static double NaturalLogE = 2.71;
    }
}
